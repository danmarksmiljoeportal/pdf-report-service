public class ReportResult
{
    /// <summary>
    /// Gets or sets the name of the report.
    /// </summary>
    public string Name { get; set; }

    /// <summary>
    /// Gets or sets the URL where the report can be accessed.
    /// </summary>
    public string Url { get; set; }

    /// <summary>
    /// Gets or sets any additional message or status about the report.
    /// </summary>
    public string Message { get; set; }
}
